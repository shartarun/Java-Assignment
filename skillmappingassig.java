public class skillmappingassig {
    public static void main(String[] args) {
        int i, k, l;
        int j;
        for (k = 5; k >= 1; k--) {

            for (l = 0; l <= k; l++) {
                System.out.print("x");
            }
            System.out.println();

        }
        for (i = 0; i < 5; i++) {

            for (j = 0; j <= i; j++) {
                System.out.print("x");
            }
            System.out.println();

        }

    }
}