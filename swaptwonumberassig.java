public class swaptwonumberassig {
    public static void main(String[] args) {
        int numA = 63;
        int numB = 15;
        System.out.println("numA=" + numA);
        System.out.println("numB=" + numB);
        System.out.println("Swap the numbers");
        int numC;
        // sw a p the numbers
        numC = numA;
        numA = numB;
        numB = numC;
        System.out.println("numA=" + numA);
        System.out.println("numB=" + numB);
    }
}
