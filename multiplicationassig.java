public class multiplicationassig {
    public static void main(String[] args) {
        int i;
        int num = 5;
        // take input values i and num
        for (i = 1; i <= 10; i++) {
            // using concatenate show 5*i=value as output
            System.out.println("5*" + i + "=" + (num * i));
        }
    }
}
